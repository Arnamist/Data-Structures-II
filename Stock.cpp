#include "Stock.h"
#include <iostream>
//*****************************************************************************************************
Stock::Stock(const std::string& name, const std::string& symbol, double price)
{
    companyName = name;
    sym = symbol;
    stockPrice = price;
};
//****************************************************************************************************************************************
Stock::Stock(const Stock& s)
{
    companyName = s.companyName;
    sym = s.sym;
    stockPrice = s.stockPrice;
}
//****************************************************************************************************************************************
void Stock::display() const
{
    std::cout << "Stock Name: " << companyName << " | Stock Symbol: " << sym << " | Stock Price: " << stockPrice << std::endl;
}
//****************************************************************************************************************************************
int Stock::hash(int size) const
{
    int index;
    index = sym.size() % size;
    return index;
}
//****************************************************************************************************************************************
std::ostream& operator << (std::ostream& out, const Stock& c)
{
    out << c.companyName << "\n";
    out << c.sym << "\n";
    out << c.stockPrice;
    return out;
};
